#include <stdio.h>
#include <math.h>

/**
  * mul - Entry Point
  *
  * Description: A C program thatv multiplies two integers
  *
  * @a: First integer
  *
  * @b: Second integer
  *
  * Return: mul returns the product of the two integers
  */

int mul(int a, int b)
{
	int product;

	product = a * b;
	return (product);
}
